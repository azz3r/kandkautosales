function setLang(lang) {
    if (!translations[lang]) return;
    document.documentElement.lang = lang;
    localStorage.setItem('kk-lang', lang);

    // Update text content
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const val = translations[lang][key];
        if (val) el.innerHTML = val;
    });

    // Update placeholders
    document.querySelectorAll('[data-i18n-ph]').forEach(el => {
        const key = el.getAttribute('data-i18n-ph');
        const val = translations[lang][key];
        if (val) el.placeholder = val;
    });

    // Update active lang button
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('lang-active', btn.dataset.lang === lang);
    });

    // Store for toast messages
    window.kkLang = lang;
}

document.addEventListener('DOMContentLoaded', () => {
    // Lang switcher clicks
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', () => setLang(btn.dataset.lang));
    });

    // Init from localStorage or default
    const saved = localStorage.getItem('kk-lang') || 'pl';
    if (saved !== 'pl') setLang(saved);
    else {
        document.querySelectorAll('.lang-btn').forEach(btn => {
            btn.classList.toggle('lang-active', btn.dataset.lang === 'pl');
        });
        window.kkLang = 'pl';
    }
});
